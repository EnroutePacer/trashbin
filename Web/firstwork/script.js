let currentLang = 'zh-CN';
let currentTheme = 'dark';

    // 滚动监听：模块淡入淡出与潮汐进入效果
document.addEventListener("DOMContentLoaded", () => {
    // 页面淡入过渡
    const transition = document.getElementById('page-transition');
    if(transition) {
        setTimeout(() => {
            transition.classList.add('loaded');
        }, 100);
    }

    const settingsMenu = document.getElementById('settings-menu');
    const settingsBtn = document.getElementById('settings-btn');
    const settingsDropdown = document.getElementById('settings-dropdown');
    
    settingsBtn.addEventListener('click', () => {
        settingsDropdown.classList.toggle('active');
    });

    settingsMenu.addEventListener('mouseleave', () => {
        settingsDropdown.classList.remove('active');
    });

    // Theme toggle
    const themeBtn = document.getElementById('theme-toggle');
    themeBtn.addEventListener('click', () => {
        if (currentTheme === 'dark') {
            currentTheme = 'light';
            document.documentElement.setAttribute('data-theme', 'light');
            themeBtn.textContent = '🌙';
        } else {
            currentTheme = 'dark';
            document.documentElement.removeAttribute('data-theme');
            themeBtn.textContent = '⚪';
        }
    });

    // Language toggle
    const langBtn = document.getElementById('lang-toggle');
    const langRotate = ['zh-CN', 'en', 'ja'];
    const langLabels = {'zh-CN': 'EN', 'en': 'JP', 'ja': '中'};
    
    langBtn.addEventListener('click', () => {
        let idx = langRotate.indexOf(currentLang);
        currentLang = langRotate[(idx + 1) % langRotate.length];
        langBtn.textContent = langLabels[currentLang];
        updateLanguage();
    });

    function updateLanguage() {
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (i18n[currentLang][key]) {
                el.textContent = i18n[currentLang][key];
            }
        });
        
        const placeholders = document.querySelectorAll('[data-i18n-placeholder]');
        placeholders.forEach(el => {
            const key = el.getAttribute('data-i18n-placeholder');
            if (i18n[currentLang][key]) {
                el.setAttribute('placeholder', i18n[currentLang][key]);
            }
        });

        // Updates title specifically
        if(document.title && document.querySelector('title[data-i18n]')) {
             const titleKey = document.querySelector('title[data-i18n]').getAttribute('data-i18n');
             if(i18n[currentLang][titleKey]) {
                 document.title = i18n[currentLang][titleKey];
             }
        }
    }

    // 平滑页面跳转
    const loginLink = document.getElementById('login-link');
    if(loginLink) {
        loginLink.addEventListener('click', (e) => {
            e.preventDefault();
            if(transition) {
                transition.classList.remove('loaded');
                setTimeout(() => {
                    window.location.href = loginLink.getAttribute('href');
                }, 800);
            } else {
                window.location.href = loginLink.getAttribute('href');
            }
        });
    }

    // 导航栏平滑滚动
    const navLinks = document.querySelectorAll('.nav-links a[href^="#"]');
    navLinks.forEach(link => {
        link.addEventListener("click", function(e) {
            e.preventDefault();
            const targetId = this.getAttribute("href");
            const targetDom = document.querySelector(targetId);
            if (targetDom) {
                // 获取导航栏高度以作偏移
                const navHeight = document.querySelector(".navbar").offsetHeight;
                const targetPosition = targetDom.getBoundingClientRect().top + window.scrollY - navHeight;
                window.scrollTo({
                    top: targetPosition,
                    behavior: "smooth"
                });
            }
        });
    });

    const modules = document.querySelectorAll(".module, .scholar-card, .paper-list, .official-links, .section-title, .interactive-part");

    const observerOptions = {
        root: null,
        rootMargin: "0px",
        threshold: 0.15
    };

    const observer = new IntersectionObserver((entries, observer) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add("visible");
                if (entry.target.classList.contains("weather-map-section") && window.weatherMapInstance) {
                    // Trigger map resize handling leafleft display issues in hidden elements
                    setTimeout(() => window.weatherMapInstance.invalidateSize(), 300);
                }
                observer.unobserve(entry.target); // 一次性动画
            }
        });
    }, observerOptions);

    modules.forEach(mod => observer.observe(mod));

    // 照片上传前端逻辑处理
    const fileInput = document.getElementById("photo-upload");
    const fileNameDisplay = document.getElementById("file-name");
    
    fileInput.addEventListener("change", function() {
        if (this.files && this.files.length > 0) {
            fileNameDisplay.textContent = "已选择: " + this.files[0].name;
            fileNameDisplay.style.color = "var(--text-primary)";
        }
    });

    const form = document.getElementById("ocean-form");
    form.addEventListener("submit", (e) => {
        e.preventDefault();
        const loc = document.getElementById("location").value;
        if(fileInput.files.length === 0) {
            alert(i18n[currentLang]['alert.nofile']);
            return;
        }
        alert(`【${loc}】 - ${i18n[currentLang]['alert.success']}`);
        form.reset();
        fileNameDisplay.textContent = i18n[currentLang]['interactive.noFile'];
    });

    // 气象与水汽溯源地图 (前端交互模拟)
    // 因无实体气象 API 连接，采用 Leaflet 配合前端模拟积雨云
    initWeatherMap();
});

function initWeatherMap() {
    window.weatherMapInstance = L.map('weather-map', {
        center: [30.0, 140.0],
        zoom: 2,
        zoomControl: false,
        attributionControl: false
    });

    // 暗色底图，符合典雅深邃的氛围 (使用更普适的CartoDB深色底图修复不可见的问题)
    L.tileLayer('https://{s}.basemaps.cartocdn.com/dark_all/{z}/{x}/{y}{r}.png', {
        subdomains: 'abcd',
        maxZoom: 19
    }).addTo(window.weatherMapInstance);

    const tooltip = document.getElementById("origin-tooltip");
    
    // 模拟的全球积雨云位置
    const clouds = [
        { lat: 15.0, lng: 130.0, origin: "此股水汽或生于西太平洋的台风眼..." },
        { lat: 40.0, lng: -40.0, origin: "这丝湿润来自北大西洋季风的叹息..." },
        { lat: -10.0, lng: 60.0, origin: "这是印度洋的热流蒸腾而上的水珠..." },
        { lat: -30.0, lng: -120.0, origin: "来自南太平洋的寒流与暖锋交汇之水..." }
    ];

    clouds.forEach(cloudData => {
        // 创建云朵div
        const myIcon = L.divIcon({
            className: 'simulated-cloud',
            iconSize: [80, 80]
        });

        const cloudMarker = L.marker([cloudData.lat, cloudData.lng], {icon: myIcon}).addTo(window.weatherMapInstance);

        cloudMarker.on('mouseover', (e) => {
            tooltip.innerText = cloudData.origin;
            tooltip.style.display = "block";
            tooltip.style.opacity = "1";
        });

        cloudMarker.on('mousemove', (e) => {
            // 将tooltip跟随鼠标，这里简单获取鼠标相对窗口位置
            const x = e.originalEvent.pageX;
            const y = e.originalEvent.pageY;
            tooltip.style.left = (x + 15) + "px";
            tooltip.style.top = (y + 15) + "px";
        });

        cloudMarker.on('mouseout', () => {
            tooltip.style.display = "none";
            tooltip.style.opacity = "0";
        });
    });
}
